        <td id="annotationcontentdescription">
            ALAN TURING WARTIME RESEARCH PAPERS RELEASED BY GCHQ
            <br>
            <br>'The Applications of Probability to Crypt'
            <br>
            <br>As the country marks the centenary of Alan Turing's birth, two of his mathematical research papers,
            believed to have been written whilst he was at Bletchley Park during World War II, have been released by
            GCHQ to The National Archives.
            <br>
            <br>Because of continuing sensitivity the papers had been retained at GCHQ, but they have now been
            reassessed as suitable for release.
            <br>
            <br>The papers, called 'Paper on Statistics of Repetitions' and 'The Applications of Probability to Crypt',
            were written in an era when typographical errors would be corrected by hand and mathematical notation
            handwritten.
            <br>
            <br>The first, 'Paper on Statistics of Repetitions', is an informal report in which Turing works out the
            best statistical means of testing whether two cipher messages use the same key in parts of the message. This
            was very important in the exploitation of such messages at Bletchley Park.
            <br>
            <br>The second, longer, paper titled 'The Applications of Probability to Cryptography', demonstrates that
            Turing was determined to apply rigorous probability analysis to a wide range of cryptanalytic problems of
            the day. A particular highlight is where Turing uses life expectancy to examine conditional probability. The
            associated example, 'Hitler is now of age 52', adds piquancy and suggests that the paper was written between
            April 1941 and April 1942. Bletchley Park's output of decrypts was almost certainly enabled by the
            techniques in this paper.
